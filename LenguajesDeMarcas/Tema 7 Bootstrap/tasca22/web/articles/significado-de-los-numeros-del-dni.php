<p>
El DNI o Documento Nacional de Identificación español se creó en 1944 por decreto y comenzó a utilizarse a partir de 1951.
Desde entonces, ha sido objeto de algunas modificaciones hasta convertirse en un documento electrónico con la incorporación de un chip en el año 2006.
Pero ¿te has preguntado cuál es el significado de los números del DNI?. Entonces, debes revisar esta interesante información:
</p>

<h2>Significado de los números del DNI: parte frontal</h2>
<p>
En la actualidad, está formado por 8 dígitos numéricos y una letra (distinta a I, Ñ, O, U).
<br />
Como imaginas, es un “código” exclusivo para cada persona y permite identificarte ante las autoridades públicas o privadas.
<br />
Este número es asignado de manera aleatoria, pero siguiendo un criterio lógico en las distintas oficias donde se tramitan.
<br />
La letra (de identificación fiscal) que acompaña a los 8 dígitos es un mecanismo de control para validar los números asignados. Pero ¿sabes cómo asigna?.
</p><p>
Para determinar la letra y validar que los datos sean correctos solo necesitas dividir los 8 dígitos entre 23. Entonces, el “resto” de la división se compara con una tabla de correspondencia en donde cada letra tiene un valor numérico asignado como aparece a continuación:
</p><p> 
Por ejemplo: si el número es 26885671, entonces se divide entre 23. El resto es igual a 5 y, por tanto, le corresponde la letra “M”. Ahora intenta conseguir la letra que le corresponde a 33587410.
</p><p>
Nota: al final del artículo, aparece la letra correspondiente.
</p><p>
Otro de los números del DNI que aparecen en la parte frontal es el número de serie de soporte que identifica a la tarjeta y, por tanto, cambia cuando se renueva el DNI. Este es un código alfanumérico formado por tres letras en mayúsculas y 6 números.
</p><p>
Adicionalmente, aparece la fecha de nacimiento del titular y la fecha de caducidad del documento.
</p>

<h2>Significado de los números del DNI: parte posterior</h2>
<p>Al reverso de tu documento de identificación también aparen una serie de dígitos que te describimos a continuación:
</p>
<h3>Equipo de expedición</h3>
<p>El equipo de expedición hace referencia a la oficina de la policía que lo emite.</p>
<p>Como sabes, se trata de un código alfanumérico de 9 dígitos y puede cambiar si renuevas tu documento de identificación en una oficina diferente a donde lo tramitaste originalmente.
</p>
<h3>Caracteres OCR</h3>
<p>Está formado por tres líneas y se utilizan en máquinas lectoras automática de este tipo de códigos.
</p><p>
La distribución por líneas es la siguiente:
</p>
<ol>
<li>1º: ID+ESP+Número de serie de soporte+Dígito de Control+DNI <<<<< </li>
<li>2º: Fecha de nacimiento en formato aa/mm/dd+Dígito de control+Sexo+Fecha de caducidad+Dígito de control+ESP <<<<<<<<<<< </li>
<li>3º: Dígito de control+Apellidos+Nombre del titular</li>
</ol>
